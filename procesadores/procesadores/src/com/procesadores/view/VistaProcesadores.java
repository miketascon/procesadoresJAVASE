  public void actualizarTabla(){
    String[] titulos={"ID", "Marca", "Modelo", "Fecha de venta", "Precio" , "Entregado"};
    
    DefaultTableModel dt= new DefaultTableModel(null, titulos);
    List<ProcesadorModel> male=malControlador.getListaProcesador();
    String[] fila = new String[6];
    for (ProcesadorModel procesadores:male){
        fila[0]=String.valueOf(procesadores.getIdProcesador());
        fila[1]=procesadores.getMarca();
        fila[2]=procesadores.getModelo();
        fila[3]=String.valueOf(procesadores.getFechaVenta());
        fila[4]=String.valueOf(procesadores.getPrecio());
        fila[5]=String.valueOf(procesadores.getEntregado());
        dt.addRow(fila);
        
    }
    tabla.setModel(dt);
  }  
    
    
