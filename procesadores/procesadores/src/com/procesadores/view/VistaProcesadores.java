    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        // TODO add your handling code here:
        //Se optiene los datos de la fila seleccionada
        int fila = tabla.getSelectedRow();
        if(fila>=0){
            tabla.getValueAt(fila, 3).toString();
            txtID.setText(tabla.getValueAt(fila, 0).toString());
            txtMarca.setText(tabla.getValueAt(fila, 1).toString());
            txtModelo.setText(tabla.getValueAt(fila, 2).toString());
            txtFecha.setText("dd/MM/yyyy".toString());
            txtPrecio.setText(tabla.getValueAt(fila, 4).toString());
            CheckEntregado.setText(tabla.getValueAt(fila, 5).toString());
            JOptionPane.showMessageDialog(null, "Por favor Volver a escribir la fecha de Venta"
                    + "");
        }else{
            JOptionPane.showMessageDialog(null, "Fila no seleccionada");
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        try {
            int fila=tabla.getSelectedRow();
            int id=Integer.parseInt(tabla.getValueAt(fila, 0).toString());
            malControlador.setId(id);
            
            if(malControlador.deleteProcesador(id)){
                JOptionPane.showMessageDialog(null,"registro eliminada Exitosamente");
                limpiar();
            }else{
                JOptionPane.showMessageDialog(null, "Erro ID no existente");
            }
            malControlador.recargarListaProcesador();
            actualizarTabla();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "ERROR FILA NO SELECCIONADA", "ERROR",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

