    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
          try {
            Date fecha = null;
            SimpleDateFormat df = new SimpleDateFormat("dd/MM/yyyy");
            String dateStr = txtFecha.getText();
            try {
                fecha = new Date(df.parse(dateStr).getTime());
            } catch (ParseException ex) {
                Logger.getLogger(VistaProcesadores.class.getName()).log(Level.SEVERE, null, ex);
            }
              malControlador.setProcesadorActual(new ProcesadorModel(Integer.parseInt(txtID.getText()), txtMarca.getText(), txtModelo.getText(), fecha, 
                      Float.parseFloat(txtPrecio.getText()), CheckEntregado.isSelected()));
            if (malControlador.addProcesador()) {
                JOptionPane.showMessageDialog(null, "Registro guardada exitosamente");
                limpiar();

            } else {
                JOptionPane.showMessageDialog(null, "Error al guardar");
            }
            malControlador.recargarListaProcesador();
            actualizarTabla();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Datos mal ingresados", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

